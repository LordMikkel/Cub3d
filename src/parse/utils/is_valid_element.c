#include "../../../inc/cube.h"

bool	is_valid_element(char c)
{
	if (c != '1' && c != '0'
		&& c != 'N' && c != 'S'
		&& c != 'E' && c != 'W'
		&& c != ' ' && c != 'X'
		&& c != 'Y' && c != 'Z'
		&& c != 'D')
		return (FALSE);
	return (TRUE);
}
